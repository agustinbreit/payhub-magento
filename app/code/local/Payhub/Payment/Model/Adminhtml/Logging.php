class Payhub_Payment_Model_Adminhtml_Logging
{
    /////////////////////////////////////////////////////////////////////////////////////////////////////

    protected $_options;

    /////////////////////////////////////////////////////////////////////////////////////////////////////

    public function toOptionArray($isMultiselect = false) {
        if (!$this->_options) {
            $this->_options = array(
                array('value' => '0', 'label' => 'No Logging'),
                array('value' => '1', 'label' => 'Normal Logging'),
                array('value' => '2', 'label' => 'Verbose Logging'),
            );
        }

        $options = $this->_options;

        return $options;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////

}

